package Block4;

public class Count100to1 {
    public static void main(String[] args) {
        for (int i = 100; i >= 1 ; i--) {
            System.out.print(i + ", ");
        }
        int j= 100;
        System.out.println();
        while (j >=1){
            System.out.print( j + ", ");
            j--;
        }
    }
}
