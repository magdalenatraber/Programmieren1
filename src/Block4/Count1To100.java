package Block4;

public class Count1To100 {
    public static void main(String[] args) {


        for (int i = 1; i <= 100; i++) {
            System.out.print(i + ", ");

        }
        int j = 1;
        System.out.println();
        while ( j <= 100){

            System.out.print(j + ", ");
            j++;

        }
    }
}
