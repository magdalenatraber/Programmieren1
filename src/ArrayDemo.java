public class ArrayDemo {
    public static void main(String[] args) {
        int[] intArray = new int [5];
        intArray[0] = 1;
        intArray[1] = 2;
        intArray[2] = 3;
        intArray[3] = 4;
        intArray[4] = 99;

        for (int i = 0; i < 5; i++) {
            System.out.print(intArray[i] + ", ");

        }


    }
}
